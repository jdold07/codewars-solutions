describe("Random tests", function () {
  let name = ""

  it("Bunch of random names", function () {
    let noOfNames = 0

    for (let i = 0; i < 50; i++) {
      name = ""
      noOfNames = ~~(Math.random() * 7 + 1)
      for (let j = 0; j < noOfNames; j++) {
        name += fn[~~(Math.random() * fn.length)] + " "
      }

      if (noOfNames > 1) {
        name += ln[~~(Math.random() * ln.length)]
      } else {
        name += Math.random() >= 0.5 ? ln[~~(Math.random() * ln.length)] : ""
      }

      name = name.trim()

      assert.strictEqual(initializeNames(name), answer(), "Incorrect value for " + name)
    }
  })

  function answer() {
    return name
      .split(" ")
      .map(function (e, i, arr) {
        return e.charAt(0) + (i == arr.length - 1 || i == 0 ? e.slice(1) : ".")
      })
      .join(" ")
  }
})
